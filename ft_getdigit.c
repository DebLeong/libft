#include "libft.h"

int	ft_getdigit(int n)
{
	int	i;
	int	digit;

	i = 0;
	digit = 0;
	if (n == -2147483648)
		return (11);
	if (n < 0)
	{
		n = -n;
		digit += 1;
	}
	while (n >= 9)
	{
		n = n / 10;
		digit += 1;
	}
	return (digit);
}
